#2. Faça um programa que peça para o usuário digitar três valores inteiro e imprima a soma deles.


num1 = int(input("escreva o primeiro numero: "))
num2 = int(input("escreva o segundo numero: "))
num3 = int(input("escreva o terceiro numero: "))
soma = num1 + num2 + num3

print(f"a soma dos numeros {num1},{num2} e {num3} é de:  {soma}")
